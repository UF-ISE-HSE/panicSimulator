            testNum(settings.vDes, 'double', [0,inf], [1, 1], [1,1]) &&...
            ischar(settings.agentPositionStyle) && ...
            (strcmp('randomLeftHalf', settings.agentPositionStyle) ||...
                strcmp('filename', settings.agentPositionStyle)) && ...
            ischar(settings.agentPositionFilename) && ...
            ischar(settings.wallPositionStyle) && ...
            (strcmp('standard', settings.wallPositionStyle) ||...
                strcmp('filename', settings.wallPositionStyle)) && ...
            testNum(settings.dt, 'double', [0,inf], [1, 1], [1,1]) &&...
            testNum(settings.dtPlot, 'double', [0,inf], [1, 1], [1,1])
        checkBool = true;
        return
    end
end
